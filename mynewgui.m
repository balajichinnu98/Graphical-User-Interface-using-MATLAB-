% --- Executes on button press in pushbutton9.
function pushbutton9_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton9 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[T,Vu] = textread('Uapp.txt');
[T,Vc] = textread('Uc.txt');
Uapp=[T,Vu];
C=22e-9;
Q=C.*Vc;
Tmax=max(T);
Uappsmooth=smoothdata(Uapp,'gaussian',7000);
Qsmooth=smoothdata(Q,'gaussian',7000);
axes(handles.axes4);
Usmooth= Uappsmooth(:,2);
f=35e+3;
M=Tmax*f;                             
PUapp=length(Usmooth)/M;                
Up=Usmooth(1:floor(PUapp));             
PUc=length(Qsmooth)/M;                 
Cp=Qsmooth(1:floor(PUc));              
area=(abs(trapz(Up,Cp)));             
power=area*f;                         
set(handles.edit7,'string',power);
guidata(hObject,handles);

